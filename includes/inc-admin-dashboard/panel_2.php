

<h2>Image Sizes Specs (W x H)</h2>
<p>
<strong>585px x 330px (GRIDS)</strong><br>
Exact Size / aspect ratio REQUIRED in Home View grid and Category View grid. Also used on 'YOU MAY ALSO LIKE' / related Reviews images<br>
Backend location > "Featured Image" field in each Post.
</p>

<hr>

<p>
<strong>1200px x 440px (SLIDER AND POST HEADERS)</strong><br>
Exact Size / aspect ratio REQUIRED in Home Slider / Review Post Header.<br>
Backend location  > "Review Header Image" field in each Post.
</p>

<hr>

<p>
<strong>1200px x 440px (CATEGORY HEADERS)</strong><br>
Exact Size / aspect ratio REQUIRED used in Categories View Header.<br>
Backend location > "Category Image" field in each Category.
</p>

<hr>

<p>
<strong>1200px x FREE Height (PAGE or POST - embedded content images)</strong><br>
Minimum recommended WIDTH for images inserted in Posts and Pages content area. Unlike the Header and Grid images, the aspect ratio is free in this case.
</p>

<hr>

<p><strong>&#8594; Note the small left and right "to-black" fade gradients used in the 1200px x 440px header images.</strong></p>
<p><strong>&#8594; Use always JPG format for photographic images.</strong></p>
<p><strong>&#8594; Use always  PNG format for plain colors or line art images.</strong></p>
