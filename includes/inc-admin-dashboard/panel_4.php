
<h2>About Backups</h2>
<p>Your website needs care. Please backup your DATABASE and FILES in a regular basis. Use tools / services provided by your hosting provider or System Administrator. If you prefer, you can use the installed "UpdraftsPlus" plugin - always at your own risk - (Settings > Updrafts Plus).</p>

<p><strong style="color:#0073aa;">Important Notes:</strong> This kind of plugin consumes hosting space, archiving backups in the server. If you need save space, consider downloading Database and Files to your local machine. After that, delete the backup from the server. We dont recommend using this tool to Restore backups. Contact your sysadmin to restore a previously downloaded / archived backup.
</p>

<h2>About Wordpress and WPML Updates</h2>
<p>This website is Multilanguage. It uses WPML + WORDPRESS. WPML is a very complex plugin, and updating without PREVIOUS testing some times can break your wordpress website. For that reason, Updates and Update notifications are DISABLED by default in this installation.</p>

<p><strong style="color:#0073aa;">We dont recommend updating LIVE without testing FIRST the impact running the website in a SAFE Testing/QA environment.</strong></p>

<p>Each wordpress release "matches" with a specific WPML release. Other plugins are not complex and "risky" with updates like WPML, but we recommend always testing every update in a SAFE environment FIRST.</p>

<p>Note: If you decide updating LIVE anyway, make a complete backup of Database and Files first. Just to be covered if something goes wrong.</p>

<p>We recommend check for updates and update your WP / WPML install every 6 months (Unless Wordpress.org launches a highest priority update).</p>

